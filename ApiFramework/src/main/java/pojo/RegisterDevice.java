package pojo;

public class RegisterDevice {
	
	private String access_id;
	private String display_name;
	private String station_type;
	private String device_model;
	private String firmware_version;
	private String user_identifier;
	private String user_identifier_type;
	private String contact_list;
	public String getAccess_id() {
		return access_id;
	}
	public void setAccess_id(String access_id) {
		this.access_id = access_id;
	}
	public String getDisplay_name() {
		return display_name;
	}
	public void setDisplay_name(String display_name) {
		this.display_name = display_name;
	}
	public String getStation_type() {
		return station_type;
	}
	public void setStation_type(String station_type) {
		this.station_type = station_type;
	}
	public String getDevice_model() {
		return device_model;
	}
	public void setDevice_model(String device_model) {
		this.device_model = device_model;
	}
	public String getFirmware_version() {
		return firmware_version;
	}
	public void setFirmware_version(String firmware_version) {
		this.firmware_version = firmware_version;
	}
	public String getUser_identifier() {
		return user_identifier;
	}
	public void setUser_identifier(String user_identifier) {
		this.user_identifier = user_identifier;
	}
	public String getUser_identifier_type() {
		return user_identifier_type;
	}
	public void setUser_identifier_type(String user_identifier_type) {
		this.user_identifier_type = user_identifier_type;
	}
	public String getContact_list() {
		return contact_list;
	}
	public void setContact_list(String contact_list) {
		this.contact_list = contact_list;
	}

}
